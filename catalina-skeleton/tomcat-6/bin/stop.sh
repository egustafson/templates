#!/bin/sh

## -- Figure out where we are directory wise.

PRG="$0"

while [ -h "$PRG" ]; do
  ls=`ls -ld "$PRG"`
  link=`expr "$ls" : '.*-> \(.*\)$'`
  if expr "$link" : '/.*' > /dev/null; then
    PRG="$link"
  else
    PRG=`dirname "$PRG"`/"$link"
  fi
done

PRGDIR=`dirname "$PRG"`

## ----------------------------------------

. "$PRGDIR/env.sh"

exec $CATALINA_HOME/bin/shutdown.sh
